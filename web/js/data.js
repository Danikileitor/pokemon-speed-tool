// Lookup: pokemon -> all tiers
function getTiersForPokemon(name) {
  return SPEED_DATA.filter(d => d.pokemon.includes(name)).sort((a, b) => b.spe - a.spe);
}

// Get speed for specific pokemon + boost + evs combo
function getSpeed(name, boost, evs) {
  const tier = SPEED_DATA.find(d =>
    d.pokemon.includes(name) &&
    d.boost === parseInt(boost) &&
    d.evs === evs
  );
  return tier ? tier.spe : null;
}

// April Fools mode flag (toggled by easter egg button)
let AFD_MODE = false;

// Pokemon sprite URL (Pokémon Showdown sprites)
// Falls back from /sprites/ani/ (animated) to /sprites/dex/ (static) via onerror in HTML
function getSpriteUrl(name) {
  const n = name.trim();

  // 1. Definimos la configuración global de la URL según el modo
  const isAFD = typeof AFD_MODE !== 'undefined' && AFD_MODE;
  const baseUrl = isAFD ? 'https://play.pokemonshowdown.com/sprites/afd/' : 'https://play.pokemonshowdown.com/sprites/ani/';
  const ext = isAFD ? '.png' : '.gif';

  // 2. Caso especial: Paldean Tauros (formas aleatorias)
  if (n === 'Paldean Tauros') {
    const forms = ['tauros-paldeacombat', 'tauros-paldeaaqua', 'tauros-paldeablaze'];
    const randomForm = forms[Math.floor(Math.random() * forms.length)];
    return baseUrl + randomForm + ext;
  }

  // 3. Identificar prefijos regionales
  const isMega = /^Mega /i.test(n);
  const isAlolan = /^Alolan /i.test(n);
  const isGalarian = /^Galarian /i.test(n);
  const isHisuian = /^Hisuian /i.test(n);
  const isPaldean = /^Paldean /i.test(n);

  // 4. Limpiar el nombre base (usamos baseName para evitar conflictos)
  let baseName = n
    .replace(/^Mega /i, '')
    .replace(/^Alolan /i, '')
    .replace(/^Galarian /i, '')
    .replace(/^Hisuian /i, '')
    .replace(/^Paldean /i, '');

  // 5. Crear el slug (formato para URL)
  let slug = baseName.toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-');

  // 6. Aplicar sufijos de Showdown
  if (isMega) {
    if (slug === 'charizard-x') slug = 'charizard-megax';
    else if (slug === 'charizard-y') slug = 'charizard-megay';
    else slug = slug + '-mega';
  }
  if (isAlolan) slug = slug + '-alola';
  if (isGalarian) slug = slug + '-galar';
  if (isHisuian) slug = slug + '-hisui';
  if (isPaldean) slug = slug + '-paldea';

  // 7. Resultado final
  return baseUrl + slug + ext;
}

// Static fallback sprite (broader coverage for custom Megas)
// In AFD mode the primary is already a PNG, fallback to /dex/ as usual
function getSpriteFallbackUrl(name) {
  if (AFD_MODE) return getSpriteUrl(name); // afd is already the best we have
  return getSpriteUrl(name).replace('/sprites/ani/', '/sprites/dex/').replace('.gif', '.png');
}


